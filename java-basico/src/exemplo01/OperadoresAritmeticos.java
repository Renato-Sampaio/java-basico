package exemplo01;

/**
 * Programa para dempntrar operações aritimeticas
 * 
 * @author rsampaio
 * @dince 05/02/2021
 */
public class OperadoresAritmeticos {
	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {
		// exemplo de adição
		int adicao;
		int subtracao;
		int multiplicacao;
		double divisao;
		int modulo;

		// exemplo de adicao
		adicao = 10 + 5;

		// exemplo de subtracao
		subtracao = 10 - 5;

		// exemplo de multiplicacao
		multiplicacao = 24 * 3;

		// exemplo de divisao
		divisao = 25 / 6.0;

		// exemplo de modulo
		modulo = 10 % 3;

		// exibindo em console
		System.out.println(" 10 + 5 =" + adicao);
		System.out.println(" 10 - 5 =" + subtracao);
		System.out.println(" 24 * 3 =" + multiplicacao);
		System.out.println(" 25 / 6 =" + divisao);
		System.out.println(" 10 % 3 =" + modulo);

		// fim
	}

}
