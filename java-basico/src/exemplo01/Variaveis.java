package exemplo01;

/**
 * Programa para demonstrar utilização de variaveis
 * 
 * @author rsampaio
 * @since 04/02/2021
 */
public class Variaveis {

	/**
	 * Metodo principal para executar o programa
	 */
	public static void main(String[] args) {
		// declarando variavel do tipo inteiro
		int idade;

		// declarando variavel do tipo String
		String nome;

		// declarando variaveis do tipo double
		double valor;

		// atribuindo valor as variaveis
		idade = 37;
		nome = "Renato Sampaio";
		valor = 155.98;

		// exibindo o conteudo da variavel
		System.out.println("Variavel Idade: " + idade + ".");
		System.out.println("Variavel Nome: " + nome + ".");
		System.out.println("Variavel valor: " + valor + ".");

		// fim
	}

}
