package exercicios;

/**
 * Programa para imprimir os numeros de 1 a 10 em ordem decrescente
 * @author Renato Sampaio
 * @since 10/02/2021
 */
public class Exercicio67 {

	/*
	 * Método principal para executar a classe
	 */
	public static void main(String[] args) {
		
		// exibindo os valores de 1 a 10 em ordem decrescente
		for (int i = 10; i > 0; i--) {
			System.out.println("Numero:" + i);
		}

	}

}
